import mongoose, { Schema } from "mongoose";
import jwt from "jsonwebtoken";
import bcrypt from "bcrypt";

const adminSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
      index: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    name: {
      type: String,
      required: false,
      trim: true,
    },
    password: {
      type: String,
      required: [true, "Password is required"],
    },
    menu: {
      type: [String],
    },
    submenu: {
      type: [String],
    },
    donations: {
      type: [String],
    },
    isSuperAdmin: {
      type: Boolean,
      required: true,
    },
    status: {
      type: Boolean,
      required: true,
    },
    refreshToken: {
      type: String,
    },
  },
  { timestamps: true }
);

